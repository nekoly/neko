#if:bool类型,数字类型
if False:
	print("条件成立")
#数字类型:非0即真(只要不是0就满足条件)
if 0:
	print("条件成立")

#if 容器类型(字符串,列表,元组,字典,集合,二进制数据)
#容器类型判断的时候,如果容器类型里面有数据那么表示条件成立,
#否则条件不成立
if "xx":
	print("哈哈")
#----------
# result = ''''''''''''
if [1,2]:
	print("抓取成功")

if (2,2):
	print("呵呵")

if {"name":"茹茹"}:
	print("在上课呢")

#非None条件成立
if not None:
	print("......")